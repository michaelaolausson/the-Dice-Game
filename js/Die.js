function Die() {
	//------------------------------------------------------------------
	//
	// PUBLIC PROPERTIES
	//
	//------------------------------------------------------------------
	this.dieValue = null;
	this.dieImg = null;
	//------------------------------------------------------------------
	//
	// CLASS GLOBALS
	//
	//------------------------------------------------------------------
	// reference to object
	var obj = this;
	//------------------------------------------------------------------
	//  init Die
	//------------------------------------------------------------------
	this.init = function() {
		obj.createDie();
	},
	//------------------------------------------------------------------
	//  create new DOM-element for Die
	//------------------------------------------------------------------
	this.createDie = function() {
		obj.dieImg = NewElem.create("img", "dice", "dice");
		Events.addListener(this.dieImg, "click", this.rollDie);
		obj.rollDie();
	},
	//------------------------------------------------------------------
	// Generate random value
	//------------------------------------------------------------------
	this.rollDie = function() {
		obj.dieValue = Math.round(Math.random() * 5) + 1;
		obj.getDieImg();
		return;
	},
	//------------------------------------------------------------------
	// Get URL for dieImg
	//------------------------------------------------------------------
	this.getDieImg = function() {
		var dieUrl = "pics/dice/" + obj.dieValue + ".png";
		obj.dieImg.src = dieUrl;
		return dieUrl;
	}
}

